public class VariableApp {
    public static void main(String[] args) {
        String myFavColor = "Black";
        int yearStarted = 2025;
        char middleInitial = 'R';
        boolean pets = true;
        String niceMess = "You have a nice smile";

        System.out.println("My favorite color is " + myFavColor);
        System.out.println("I started this class in " + yearStarted);
        System.out.println("My middle initial is " + middleInitial);
        System.out.println("If someone asked if I have pets I would say: " + pets);
        System.out.println("Here's a nice message for you: " + niceMess);
    }
}
